Підсумок

Було проведено A/B тест для оцінки впливу підвищення знижки на ключові бізнес-метрики.

Основна метрика (середній чек) не продемонструвала статистично значущих змін 
(p = 0.8139), а спостережуваний ефект становить лише +2.02%, що значно нижче 
мінімально виявлюваного ефекту (MDE ≈ 24.02%). Це означає, що поточний тест не 
має достатньої чутливості для виявлення таких малих змін, а отриманий ефект не 
може вважатися надійним.
Guardrail-метрика (freight_ratio) показала статистично значуще покращення 
(p = 0.0439), що свідчить про зниження частки витрат на доставку відносно чеку 
та покращення юніт-економіки.
Secondary-метрика (кількість товарів у замовленні) не продемонструвала статистично
значущих змін (p = 0.2865), що вказує на відсутність поведінкового ефекту з боку
користувачів.

