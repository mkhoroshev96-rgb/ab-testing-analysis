Висновок

Було проведено А/В тест для оцінки впливу змін на середній чек замовлення. 
СТатистично значущої різниці між групами не виявлено (р = 0,8139), а спостережуваний
ефект становить 2,02%. Розрахунок MDE показав, що тест здатен надійно виявляти 
ефект від 24,02% і вище. Оскільки фактичний ефект є значно меншим за цей поріг,
поточний експеримент не має достатньої чутливості для виявлення такої різниці.
Таким чином, за результатами тесту немає підстав стверджувати, що зміна позитивно
вплинула на середній чек замовлення.

