library(dplyr)
library(boot)
library(pwr)

set.seed(543)

# 1. sample size

p1 <- 0.05
p2 <- 0.08

h <- ES.h(p1, p2)

n <- ceiling(pwr.2p.test(h = h, sig.level = 0.05, power = 0.8)$n * 2)

# 2.Дані

df <- data.frame(
  group = sample(c("A", "B"), n, replace = T),
  segment = sample(c("low", "mid", "high"), n, replace = T, prob = c(0.5,0.3,0.2)),
  day = sample(1:28, n, replace = T))

# 2.1 SRM check (чи рівні групи)

chisq.test(table(df$group), p = c(0.5, 0.5))

Показник р становить 0,2293, що значить, що перекосу між групами немає.

# 2.2 Balance check (чи рівномірні сегменти)

chisq.test(table(df$group, df$segment))

Показник р становить 0,5167, що значить, що сегменти між обома групами розподілені
рівномірно.

# 3. CTR (0/1 — клікнув чи ні)

df$prob <- 0

# група А

df$prob[df$group == "A" & df$segment == "high"] <- 0.15
df$prob[df$group == "A" & df$segment == "mid"] <- 0.12
df$prob[df$group == "A" & df$segment == "low"] <- 0.10

# група В

df$prob[df$group == "B" & df$segment == "high"] <- 0.16
df$prob[df$group == "B" & df$segment == "mid"] <- 0.13
df$prob[df$group == "B" & df$segment == "low"] <- 0.11

# генерація

df$ctr <- rbinom(n, 1, df$prob)

# 4. Bounce rate (0/1 — вийшов чи ні)

df$prob_bounce <- 0

# група А

df$prob_bounce[df$group == "A" & df$segment == "high"] <- 0.25
df$prob_bounce[df$group == "A" & df$segment == "mid"] <- 0.35
df$prob_bounce[df$group == "A" & df$segment == "low"] <- 0.45

# група В 

df$prob_bounce[df$group == "B" & df$segment == "high"] <- 0.28
df$prob_bounce[df$group == "B" & df$segment == "mid"] <- 0.38
df$prob_bounce[df$group == "B" & df$segment == "low"] <- 0.48

# Генерація

df$bounced <- rbinom(n, 1, df$prob_bounce)

# 5. основні тести

# Правило прийняття рішеннь перед аналізом:
# p-value для SRM > 0.05
# Основна метрика(CTR): p-value < 0.05
# 95% bootstrap довірчий інтервал для uplift не включає 0
# Bounced не є статистично значуще гіршою, ніж в контрольній групі

# Bounced(Guardrail - метрика)

tab <- table(df$group, df$bounced)
prop.test(tab[,2], rowSums(tab))

Показник р становить 0,4151, що значить, що не має суттєвої статистично значущої
різниці між обома групами 

# CTR

tab1 <- table(df$group, df$ctr)
prop.test(tab1[ ,2], rowSums(tab1))

Показник р становить 0,04851, що значить, що існує деяка статистично значуща різниця
між обома групами.

# uplift

uplift <- with(df, (mean(ctr[df$group == "B"]) - mean(ctr[df$group == "A"])) / 
                 mean(ctr[df$group == "A"]))

Величина спостережуваного ефекту становить 25,71%. Тобто, CTR в групі В вищий на 
вищезазначену величину, ніж в групі А

# Bootstrap CI (начкільки стабільний ефект)

boot_fn <- function(d, i) {
  x <- d[i, ]
  (mean(x$ctr[x$group == "B"]) - mean(x$ctr[x$group == "A"])) / mean(x$ctr[x$group == "A"])
}

boot_res <- boot(df, boot_fn, R = 1000)

ci <- quantile(boot_res$t, c(0.025, 0.975))

Отже, довірчий інтервал не включає в себе 0(0.011;0.541), що свідчить про те, що
ефект все ж є стійким та статистично значущим. 

# Висновки 

У ході А/В тесту було проведено зміни в групі В на метрики Bounced і CTR.

SRM та баланс сегментів не виявили статистично значущих відмінностей(р > 0.05), 
що значить, що групи сформовані корректно.

Bounced(Guardrail-метрика): Показник р становить 0,4151, що свідчить про віжсутність
статистично значущої різниці між групами по цій метриці

CTR(Primary-метрика): Показник р становить 0,04851, що свідчить про статистично 
значущу різницю між групами, однак ефект знаходиться на межі рівня значущості.

uplift: Спостерігається доволі сильний uplift(25.71%), який є статистично значущим,
однак в абсолютному значенні різниця залишається невеликою.

Довірчий інтервал: не включає в себе 0, що свідчить про статистично значущий ефект,
стабільний за знаком, але з варіацією по величині.

Згідно з попередньо визначеним правилом прийняття рішеннь, основна метрика досягла
статистично значущої різниці.

Фінальне рішення: Фічу доцільно запускати з поступовим rollout, оскільки:
- primary метрика покращилась (p < 0.05)
- guardrail метрика не погіршилась
- bootstrap CI не включає 0

