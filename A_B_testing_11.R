Контекст

Тестуємо фічу: “показ орієнтовної дати доставки прямо в картці товару”

Метрики

Primary: CTR on product page — чи частіше користувачі переходять у картку товару

Guardrail: bounce_rate — чи не зросте частка користувачів, які одразу виходять 
через завищені очікування або недовіру

Гіпотеза

H0: фіча не впливає на CTR
H1: CTR зросте

Логіка

користувач одразу бачить, коли орієнтовно отримає товар
це знижує невизначеність і підвищує зацікавленість → CTR ↑
але частина користувачів може побачити занадто довгу доставку і піти → bounce_rate ↑

Потенційний ризик

CTR росте, але одночасно росте bounce rate → фіча привертає увагу, але погіршує 
якість трафіку

