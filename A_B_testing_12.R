Контекст

Тестуємо фічу: показ “безкоштовна доставка від $100” на сторінці товару

Метрики

Primary: AOV (average order value) — чи зросте середній чек
Guardrail: Guardrail: freight (вартість доставки) — чи не зміниться вартість 
доставки небажаним чином

Гіпотеза

H0: фіча не впливає на середній чек
H1: середній чек зросте

Логіка

користувач бачить, що при досягненні порогу $100 доставка стає безкоштовною
→ це мотивує додати ще товар у корзину → AOV ↑
але при цьому частина замовлень отримує дешевшу або безкоштовну доставку → freight ↓

Потенційний ризик
середній чек може зрости, але через частіше застосування безкоштовної або дешевшої
доставки економічний ефект для бізнесу може виявитися слабким

А/В тест 12

library(boot)
library(dplyr)
library(pwr)

set.seed(67)

# 1. sample size 

effect_d <- (1000 * 0.03)/250

n <- ceiling(pwr.t.test(d = effect_d, sig.level = 0.05, power = 0.8, 
                        type = "two.sample")$n * 2)

# 2. Дані

df <- data.frame(
  group = sample(c("A","B"), n, replace = T),
  segment = sample(c("low","mid","high"), n, replace = T, prob = c(0.5, 0.3, 0.2)),
  day = sample(1:28, n, replace = T))

# 2.1 SRM check (чи рівні групи)

chisq.test(table(df$group), p = c(0.5, 0.5))

Показник р становить 0,1642,Ж що сідчить про те, що немає суттєвої статистично
значущої різниці між розподілами обох груп

# 2.2 Balance check (чи рівномірні сегменти)

chisq.test(table(df$group, df$segment))

Показник р становить 0,09235, що зрештою вказує на те, що сегменти між обома
групами розподілені рівномірно.

# 3. Генерація AOV

meanlog_vec <- ifelse(df$segment == "high", log(1500),
               ifelse(df$segment == "mid", log(800), log(500)))

df$aov <- rlnorm(n, meanlog = meanlog_vec, sdlog = 0.5)

# Сезонність

df$aov <- df$aov * ifelse(df$day %in% c(6, 13, 20, 27), 1.12, 1)

# treatment effect

df$aov[df$group == "B"] <- df$aov[df$group == "B"] * runif(sum(df$group == "B"), 1.03, 1.08)

# outliers

out_idx <- sample(1:n, size = round(0.05 * n))

df$aov[out_idx] <- df$aov[out_idx] * runif(length(out_idx), 4, 9)

# 4. Генерація freight

df$freight <- df$aov * runif(n, 0.08, 0.15)

# у групі В частіше безкоштовна або дешевша доставка

df$freight[df$group == "B"] <- df$freight[df$group == "B"] * runif(sum(df$group == "B"), 0.5, 0.85)

# 5. Основні тести

# Правило прийняття рішеннь перед аналізом:
# p-value для  SRM > 0.05
# Основна метрика (AOV): p-value < 0.05
# 95% bootstrap CI для uplift не включає в себе 0
# freight не повинна змінитися у небажаному для бізнесу напрямку

# freight (Guardrail-метрика)

t.test(freight~group, data = df)

Показник p-value становить 3.761e-07, що свідчить про наявність статистично значущої
різниці між групами за метрикою freight. Тобто фіча суттєво вплинула на вартість доставки.
Напрямок цієї зміни потрібно оцінювати окремо через середні значення в групах A і B.

df %>% group_by(group) %>% summarise(mean_freight = mean(freight),
                                     median_freight = median(freight),
                                     sd_freight = sd(freight))

Середня вартість доставки в групі B є нижчою (97.1), ніж у групі A (130.7),
що свідчить про те, що впроваджена фіча призвела до зменшення вартості доставки.

Це узгоджується з логікою експерименту: частина замовлень у групі B отримує
дешевшу або безкоштовну доставку.

Таким чином, зміна guardrail-метрики має позитивний характер.

# AOV (Основна метрика)

t.test(aov~group, data = df)

Показник р становить 0,09444, що значить, що суттєвої статистично значущої різниці
між обома групами немає. 

# uplift (на скільки % змінилось)

uplift <- with(df, (mean(aov[df$group == "B"]) - mean(aov[df$group == "A"])) /
                      mean(aov[df$group == "A"]))

Величина спостережуваного ефекту становить 10,04%, тобто середній чек у групі B
в середньому на 10,04% вищий, ніж у групі A.

# 6. Bootstrap CI (Наскільки стабільний ефект)

boot_fn <- function(d, i) {
  x <- d[i, ]
  (mean(x$aov[x$group == "B"]) - mean(x$aov[x$group == "A"])) / mean(x$aov[x$group == "A"])
}

boot_res <- boot(df, boot_fn, R = 1000)

ci <- quantile(boot_res$t, c(0.025, 0.975))

Довірчий інтервал включає 0, що свідчить про нестійкість ефекту: спостережувана
різниця може бути випадковою.

# 7. Висновки

У ході A/B тесту в групі B було змодельовано вплив фічі на середній чек та 
вартість доставки.

SRM та баланс сегментів не виявили статистично значущих відмінностей, групи 
сформовані коректно.

Вартість доставки: p-value становить 3.761e-07, що свідчить про наявність статистично
значущої різниці між групами.

Середній чек: Показник р становить 0,09444, що значить, що немає суттєвої статистично
значущої різниці між групами. Тобто, фіча не вплинула на дану метрику.

Uplift: спостерігається помірне зростання середнього чеку (+10,04%), але воно не
є статистично підтвердженим.

Довірчий інтервал включає 0, а отже ефект не є стійким і може бути випадковим.

Згідно з попередньо визначеним правилом прийняття рішень, основна метрика не
досягла статистичної значущості.

Фінальне рішення: фічу не запускати, оскільки основна метрика (AOV) не досягла
статистичної значущості.

Попри те, що guardrail-метрика freight статистично значущо знизилась, що є
позитивним ефектом, зростання середнього чеку не було підтверджено.

Таким чином, фіча не демонструє стабільного позитивного впливу на ключову 
бізнес-метрику, а отже її запуск на даному етапі є передчасним.